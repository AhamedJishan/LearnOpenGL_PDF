		shader.SetVec3("pointLight[0].position", pointLightPositions[0]);
		shader.SetVec3("pointLight[0].ambient", 0.1f, 0.1f, 0.1f);
		shader.SetVec3("pointLight[0].diffuse", 0.5f, 0.5f, 0.5f);
